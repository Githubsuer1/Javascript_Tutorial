// if else statements are used to work with many alternatives, if works only if condition true otherwise else exicuted.

// simple if else statement
let age = 60;
if (age>60){
    console.log("Yes you are eligible to get pention");
}
else{
    console.log("Wait! You are not 60 year old.")
}

// if else if  statement

let marks = 75;

if (marks > 70){
    console.log("Congratulations! you have got first division");
}
else if(marks > 55){
    console.log("You have got second division.")
}
else if(marks > 33){
    console.log("you are just passed with third division")
}
else{
    console.log("oops! you are fail");
}

